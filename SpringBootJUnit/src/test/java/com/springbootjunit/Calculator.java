package com.springbootjunit;

public class Calculator {

    //Addition
    public int doSum(int a, int b, int c) {
        return (a + b + c);
    }

    //Multiplication
    public int doProduct(int a, int b, int c) {
        return (a * b * c);
    }

    //Boolean
    public boolean compareTwoNo(int a, int b) {
        return (a == b);
    }
}
